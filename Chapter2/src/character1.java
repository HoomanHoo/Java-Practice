
public class character1 {

	public static void main(String[] args) {
		char ch = 'A';
		
		System.out.println(ch);
		System.out.println((int)ch); //ch의 값을 정수형으로 출력
		
		
		ch = 66;
		System.out.println(ch);  
		
		int ch2 = 67;
		System.out.println(ch2);
		System.out.println((char)ch2); //int형으로 선언한 변수값을 char형으로 표현
		
	}

}
